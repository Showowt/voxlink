// Build reverse lookups (target→en) for all languages
const REVERSE_MAP: Record<string, Record<string, string>> = {};
for (const [lang, phrases] of Object.entries(EN_PHRASES)) {
  REVERSE_MAP[lang] = {};
  for (const [en, translated] of Object.entries(phrases)) {
    REVERSE_MAP[lang][translated.toLowerCase()] = en;
  }
}

/**
 * Translate using the offline dictionary.
 * Uses English as pivot for non-English pairs.
 * Returns null if phrase not found.
 */
export function offlineTranslate(
  text: string,
  from: string,
  to: string,
): string | null {
  const normalized = text.toLowerCase().trim();
  if (!normalized) return null;
  if (from === to) return text;

  // Direct: en → target
  if (from === "en") {
    return EN_PHRASES[to]?.[normalized] ?? null;
  }

  // Direct: source → en
  if (to === "en") {
    return REVERSE_MAP[from]?.[normalized] ?? null;
  }

  // Pivot: source → en → target
  const english = REVERSE_MAP[from]?.[normalized];
  if (english) {
    return EN_PHRASES[to]?.[english] ?? null;
  }

  return null;
}

/**
 * Get all supported language codes in the dictionary
 */
export function getSupportedLanguages(): string[] {
  return ["en", ...Object.keys(EN_PHRASES)];
}
